function initNavHighlight() {
  const sections = ["quran", "dua", "athkar", "tasbeeh"];
  const navMap = {
    quran: document.querySelectorAll(".nav-q"),
    dua: document.querySelectorAll(".nav-d"),
    athkar: document.querySelectorAll(".nav-a"),
    tasbeeh: document.querySelectorAll(".nav-t"),
  };
  const pills = document.querySelectorAll(".nav-pill");
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        const id = entry.target.id;
        if (navMap[id]) {
          pills.forEach((p) => p.classList.remove("active"));
          if (entry.isIntersecting)
            navMap[id].forEach((el) =>
              el.closest(".nav-pill")?.classList.add("active"),
            );
        }
      });
    },
    { threshold: 0.3 },
  );
  sections.forEach((id) => {
    const el = document.getElementById(id);
    if (el) observer.observe(el);
  });
}

