function toMinutes(timeStr) {
  const parts = timeStr.split(":");
  return parseInt(parts[0], 10) * 60 + parseInt(parts[1], 10);
}

async function initAlerts() {
  const today = new Date();
  const dd = String(today.getDate()).padStart(2, "0");
  const mm = String(today.getMonth() + 1).padStart(2, "0");
  const yyyy = today.getFullYear();
  const nowMin = today.getHours() * 60 + today.getMinutes();
  let isArafatDay = false,
    isMorningTime = false,
    isEveningTime = false;
  try {
    const [hijriRes, timesRes] = await Promise.all([
      fetch(`https://api.aladhan.com/v1/gToH/${dd}-${mm}-${yyyy}`, {
        signal: AbortSignal.timeout(6000),
      }),
      fetch(
        `https://api.aladhan.com/v1/timingsByCity/${dd}-${mm}-${yyyy}?city=Nablus&country=Palestine&method=3`,
        { signal: AbortSignal.timeout(6000) },
      ),
    ]);
    const hijriJson = await hijriRes.json();
    if (hijriJson.code === 200) {
      const hDay = parseInt(hijriJson.data.hijri.day, 10);
      const hMonth = parseInt(hijriJson.data.hijri.month.number, 10);
      isArafatDay = hDay === 9 && hMonth === 12;
    }
    const timesJson = await timesRes.json();
    if (timesJson.code === 200) {
      const t = timesJson.data.timings;
      const fajr = toMinutes(t.Fajr);
      const asr = toMinutes(t.Asr);
      const maghrib = toMinutes(t.Maghrib);
      isMorningTime = nowMin >= fajr && nowMin < 12 * 60;
      isEveningTime = nowMin >= asr && nowMin < maghrib;
    }
  } catch (e) {
    console.warn("Alert API failed:", e);
  }

  const chain = [];
  if (isArafatDay) chain.push("arafatAlertOverlay");
  if (isMorningTime) chain.push("morningAthkarOverlay");
  if (isEveningTime) chain.push("eveningAthkarOverlay");
  chain.push("diabetesAlertOverlay");

  const closeFnMap = {
    arafatAlertOverlay: "closeArafatAlert",
    morningAthkarOverlay: "closeMorningAthkar",
    eveningAthkarOverlay: "closeEveningAthkar",
    diabetesAlertOverlay: "closeDiabetesAlert",
  };

  function showNext(index) {
    if (index >= chain.length) return;
    const id = chain[index];
    const fnName = closeFnMap[id];
    document.getElementById(id).style.display = "flex";
    const orig = window[fnName];
    window[fnName] = function () {
      orig();
      window[fnName] = orig;
      setTimeout(() => showNext(index + 1), 300);
    };
  }
  showNext(0);
}

window.addEventListener("DOMContentLoaded", initAlerts);
