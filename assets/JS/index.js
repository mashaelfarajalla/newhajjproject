function renderDua() {
  if (!ADIYA_DATA) {
    document.getElementById("duaDisplay").innerHTML =
      `<div style="text-align:center;color:var(--text-muted);padding:40px;">${UI[gLang].loading}</div>`;
    return;
  }
  const category = ADIYA_DATA.categories[curDuaCat];
  const rtl = ["ar", "fa", "ur"].includes(gLang);
  const dir = rtl ? "rtl" : "ltr";
  let duas =
    category.locations && category.locations.length > 0
      ? category.locations[curDuaLoc].duas
      : category.duas || [];
  let html = "";
  duas.forEach((d) => {
    const trans = getTranslation(d.translations, gLang);
    const isAr = gLang === "ar";
    html += `<div class="dua-item">`;
    html += `<div class="arabic-text">${d.text}</div>`;
    if (!isAr && trans) {
      html += `<div class="trans-text" style="direction:${dir};text-align:${dir === "ltr" ? "left" : "right"};">${trans}</div>`;
    } else if (isAr && d.translations && d.translations.ar) {
      html += `<div class="trans-text" style="direction:rtl;text-align:right;color:var(--text-muted);font-size:1.15em;">${d.translations.ar}</div>`;
    }
    if (d.source) {
      const src =
        typeof d.source === "object"
          ? d.source[gLang] || d.source.ar
          : d.source;
      html += `<div class="dua-source"><span><i class="fa-solid fa-quote-right"style="color:var(--gold);"></i> </span>${src}</div>`;
    }
    html += `</div>`;
  });
  document.getElementById("duaDisplay").innerHTML =
    html ||
    `<div style="text-align:center;color:var(--text-muted);padding:40px;">—</div>`;
}

