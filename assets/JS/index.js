function setGoal(g, btn) {
  goal = g;
  document
    .querySelectorAll(".goal-btn")
    .forEach((b) => b.classList.remove("active"));
  btn.classList.add("active");
  updateRing();
  document.getElementById("counterGoal").textContent = "/ " + toLocalNum(goal);
}
function tapCounter() {
  count++;
  totalCount++;
  updateDisplay();
  if (navigator.vibrate) navigator.vibrate(30);
  if (count >= goal) {
    const btn = document.getElementById("tapBtn");
    btn.classList.add("complete");
    setTimeout(() => {
      btn.classList.remove("complete");
      count = 0;
      updateDisplay();
    }, 2000);
  }
}
function undoCounter() {
  if (count > 0) {
    count--;
    totalCount = Math.max(0, totalCount - 1);
    updateDisplay();
  }
}
function resetCounter() {
  count = 0;
  updateDisplay();
}

// convert numbers: Arabic for RTL langs, Western for LTR
function toLocalNum(n) {
  if (["ar", "fa", "ur"].includes(gLang)) {
    return String(n).replace(/\d/g, (d) => "٠١٢٣٤٥٦٧٨٩"[d]);
  }
  return String(n);
}

function updateDisplay() {
  document.getElementById("counterNum").textContent = toLocalNum(count);
  document.getElementById("counterGoal").textContent = "/ " + toLocalNum(goal);
  document.getElementById("totalCount").textContent = toLocalNum(totalCount);
  updateRing();
}
function updateRing() {
  const pct = Math.min(count / goal, 1);
  document.getElementById("ringProgress").style.strokeDashoffset =
    CIRCUMFERENCE * (1 - pct);
}

