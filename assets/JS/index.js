async function loadModalSurah(surah, scrollTop = true) {
  modalLang = gLang;
  modalSurah = surah;
  modalPage = 0;
  buildModalSurahList();
  document.getElementById("modalAyahDisplay").innerHTML =
    `<div class="loading-wrap"><div class="spinner-ring"></div><span>${UI[gLang].loading}</span></div>`;
  try {
    const arKey = `${surah.n}_ar`;
    const transKey = `${surah.n}_${modalLang}`;
    const fetches = [];
    if (!apiCache[arKey])
      fetches.push(
        fetch(`https://api.alquran.cloud/v1/surah/${surah.n}/quran-uthmani`)
          .then((r) => r.json())
          .then((d) => {
            apiCache[arKey] = d.data.ayahs;
          }),
      );
    if (modalLang !== "ar" && !apiCache[transKey])
      fetches.push(
        fetch(
          `https://api.alquran.cloud/v1/surah/${surah.n}/${EDITION_MAP[modalLang]}`,
        )
          .then((r) => r.json())
          .then((d) => {
            apiCache[transKey] = d.data.ayahs;
          }),
      );
    await Promise.all(fetches);
    renderModalAyahs();
    if (scrollTop) document.querySelector(".modal-content-area").scrollTop = 0;
  } catch (e) {
    document.getElementById("modalAyahDisplay").innerHTML =
      `<div class="loading-wrap" style="color:#e08080;">تعذّر التحميل — تحقق من الاتصال</div>`;
  }
}
function stripBismillah(text) {
  return text
    .replace(
      /^[بب]ِ?سْ?مِ? [ٱا]للّ?َ?هِ? [ٱا]لرّ?َ?حْ?مَ?ٰ?نِ? [ٱا]لرّ?َ?حِ?يمِ?\s*/u,
      "",
    )
    .replace(/^بسم الله الرحمن الرحيم\s*/, "")
    .trim();
}
function renderModalAyahs() {
  const s = modalSurah;
  modalLang = gLang;
  const arAyahs = apiCache[`${s.n}_ar`];
  const transAyahs =
    modalLang !== "ar" ? apiCache[`${s.n}_${modalLang}`] : null;
  if (!arAyahs) return;
  const rtl = ["ar", "fa", "ur"].includes(modalLang);
  const dir = rtl ? "rtl" : "ltr";
  const total = arAyahs.length,
    totalPages = Math.ceil(total / AYAHS_PER_PAGE);
  const start = modalPage * AYAHS_PER_PAGE,
    end = Math.min(start + AYAHS_PER_PAGE, total);
  let html = "";
  document.getElementById("modal-title-text").textContent = `${s.ar} — ${s.en}`;
  if (start === 0)
    html += `<div class="bismillah-modal">بِسْمِ اللَّهِ الرَّحْمَٰنِ الرَّحِيمِ</div>`;
  let arFlow = "";
  for (let i = start; i < end; i++) {
    let arText = arAyahs[i].text;
    const isBismillahAyah = i === 0 && stripBismillah(arText).trim() === "";
    if (isBismillahAyah) continue;
    if (i === 0) arText = stripBismillah(arText);
    arFlow += `${arText} <span class="ayah-num-badge">${i + 1}</span> `;
  }
  html += `<div class="ayah-flow" style="direction:rtl;text-align:right;">${arFlow}</div>`;
  if (transAyahs) {
    html += `<div class="trans-block">`;
    for (let i = start; i < end; i++) {
      let transText = transAyahs[i].text;
      if (!rtl) transText = transText.replace(/﴿/g, "(").replace(/﴾/g, ")");
      html += `<div class="trans-line" style="direction:${dir};text-align:${dir === "ltr" ? "left" : "right"};"><span class="trans-num">${i + 1}.</span> ${transText}</div>`;
    }
    html += `</div>`;
  }
  if (totalPages > 1) {
    html += `<div class="pagination-row">
      <button class="page-btn" onclick="changeModalPage(-1)" ${modalPage === 0 ? "disabled" : ""}>◀</button>
      <span class="page-info">${modalPage + 1} / ${totalPages}</span>
      <button class="page-btn" onclick="changeModalPage(1)" ${modalPage >= totalPages - 1 ? "disabled" : ""}>▶</button>
    </div>`;
  }
  document.getElementById("modalAyahDisplay").innerHTML = html;
}
function changeModalPage(dir) {
  const total = apiCache[`${modalSurah.n}_ar`].length;
  const totalPages = Math.ceil(total / AYAHS_PER_PAGE);
  modalPage = Math.max(0, Math.min(totalPages - 1, modalPage + dir));
  renderModalAyahs();
  document.querySelector(".modal-content-area").scrollTop = 0;
}

