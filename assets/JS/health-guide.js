// HEX CANVAS
// ══════════════════════════════════════
(function () {
  const canvas = document.querySelector(".hero-hex-canvas");
  if (!canvas) return;
  const hero = canvas.parentElement;
  function draw() {
    canvas.width = hero.offsetWidth;
    canvas.height = hero.offsetHeight;
    const ctx = canvas.getContext("2d");
    const size = 90;
    const w = size * 2;
    const h = Math.sqrt(3) * size;
    const cols = Math.ceil(canvas.width / (w * 0.75)) + 2;
    const rows = Math.ceil(canvas.height / h) + 2;
    ctx.strokeStyle = "rgba(200, 151, 42, 0.03)";
    ctx.fillStyle = "rgba(200, 151, 42, 0.008)";
    ctx.lineWidth = 1;
    for (let r = -1; r < rows; r++) {
      for (let c = -1; c < cols; c++) {
        const x = c * w * 0.75;
        const y = r * h + (c % 2 === 0 ? 0 : h / 2);
        ctx.beginPath();
        for (let i = 0; i < 6; i++) {
          const a = (Math.PI / 3) * i - Math.PI / 6;
          const px = x + size * Math.cos(a);
          const py = y + size * Math.sin(a);
          i === 0 ? ctx.moveTo(px, py) : ctx.lineTo(px, py);
        }
        ctx.closePath();
        ctx.fill();
        ctx.stroke();
      }
    }
  }
  draw();
  window.addEventListener("resize", draw);
})();
